# Задание №2
# Создайте в переменной data список значений разных типов перечислив их через
# запятую внутри квадратных скобок. Для каждого элемента в цикле выведите:
# ✔ порядковый номер начиная с единицы
# ✔ значение
# ✔ адрес в памяти
# ✔ размер в памяти
# ✔ хэш объекта
# ✔ результат проверки на целое число только если он положительный
# ✔ результат проверки на строку только если он положительный
# Добавьте в список повторяющиеся элементы и сравните на результаты
import sys

data = [42, 3.14, "Hello, World!", True, 42, "Python", 3.14]

for i in range(len(data)):
    element = data[i]

    print(f"Порядковый номер: {i + 1}")
    print(f"Значение: {element}")
    print(f"Адрес в памяти: {id(element)}")
    print(f"Размер в памяти: {sys.getsizeof(element)} байт")
    print(f"Хэш объекта: {hash(element)}")

    if isinstance(element, int) and element > 0:
        print("Результат проверки на целое число: True")

    if isinstance(element, str):
        print("Результат проверки на строку: True")

    print()
